package org.wso2.usermanager.acl.realm;

import org.wso2.usermanager.UserManagerException;

/**
 * UnAuthorizedException is thrown when a user is not authorized.
 */
public class UnauthorizedException
        extends UserManagerException {

    private static final long serialVersionUID = -1020471015718707869L;

    public UnauthorizedException(String message) {
        super(message);
    }

    public UnauthorizedException(String message, Throwable cause) {
        super(message, cause);
    }

    public UnauthorizedException(String key, Object[] args) {
        super(key, args);
    }
}
