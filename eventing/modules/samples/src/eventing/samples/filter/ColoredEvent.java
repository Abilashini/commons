package eventing.samples.filter;

import org.wso2.eventing.Event;

/**
 * A specific event type which has color.  An example of using an Event
 * subclass - if there were a wire representation (such as XML), this would need
 * to be data bound to/from the correct serialization.
 */
public class ColoredEvent extends Event {
    private String color;

    public ColoredEvent(String color) {        
        this.color = color;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
