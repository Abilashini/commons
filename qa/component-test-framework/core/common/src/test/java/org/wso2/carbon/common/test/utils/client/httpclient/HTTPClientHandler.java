package org.wso2.carbon.common.test.utils.client.httpclient;

import org.apache.commons.httpclient.methods.DeleteMethod;
import org.apache.commons.httpclient.methods.GetMethod;
import org.apache.commons.httpclient.methods.HeadMethod;
import org.apache.commons.httpclient.methods.OptionsMethod;
import org.apache.commons.httpclient.methods.PostMethod;
import org.apache.commons.httpclient.methods.PutMethod;

/**
 *  Having methods of HTTP 1.1 spec
 */
public interface HTTPClientHandler {
    public GetMethod httpGet(String url);
    public HeadMethod httpHead(String url);
    public PostMethod httpPost(String url);
    public PutMethod httpPut(String url);
    public DeleteMethod httpDelete(String url);
    public OptionsMethod httpOption(String url);


}
